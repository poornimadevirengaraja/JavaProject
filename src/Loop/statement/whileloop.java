package Loop.statement;
/* 
 * while loop: Executes the block of code as long as the specified condition is true
 * ===========
 * syntax:
 * =======

	while(condition){
	//code;
	}
	
 */
public class whileloop {
	public static void main(String[] args) {
		int i=0;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
	}

}
