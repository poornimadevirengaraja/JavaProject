package arguments;

public class Switch {
	int a = 50;

	public void swithcmethod() {
		
		switch (a) {
		case 50:
		{
			System.out.println("value is 50");
			break;// break stops the execution
			//continue;//helps to continue the execution
		}
		
	
		case 10:
		{
			System.out.println("value is 10");
			break;
		}
		
		default:
		{
			System.out.println("value is 30");
			
		}
			
		}			

	}
}
